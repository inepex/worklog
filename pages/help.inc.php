	<h4>Bevezetés</h4>
	<p>A Worklog célja, hogy egyszerűen tudd vezetni, hogy <b>mikor mennyit melyik projekten dolgoztál</b>.
	<br/> <b>Annyival ad többet</b>, mint a korábbi rendszer, hogy <b>kimutatásokat, statisztikákat</b> tudunk készíteni a projektekről és a munkádról, továbbá tervezhetővé vállnak a projektek.</p>

	
	<h5>Mi a projekt?</h5>
	<p>Projekt alatt egy körülbelül egy hónapnyi, egy jól meghatározható cél elérése érdekében létrehozott tevékenységsorozatot értünk. <br/>Alapvetően kétféle projekt létezik:</p>
	<ul>
	<li><b>Egyszeri projekt:</b> Meg nem ismételhető projekt: pl. Markerek bevezetése, OSM térkép bevezetése, Viszonteladói árak kidolgozása, Plakát készítése Cebit-re, Kötelező adatszolgáltatás megvalósítása...stb.</li>
	<li><b>Megújuló projekt:</b> Adminisztratív dolgok, ismétlődő dolgok: pl. SEO (Adwords&Analytics), Tanulás, Trackerek inicializálása, Ügyelet ..stb</li>
	</ul>
	<p>Ahhoz azonban, hogy a kimutatásokat hó végén meg tudjuk nézni, a megújuló projekteket is havonta létre kell hozni.</p>
		
	<h5>A projekt alapvető részei</h5>
	<ul>
	<li><b>Projektvezető, és résztvevők:</b> Akik a projekten dolgoznak</li>
	<li><b>Projekt neve, cég:</b> Pontos leírás a könnyebb beazonosíthatóság érdekében</li>
	<li><b>Leírás (description):</b> Miről szól a projekt érthetően.</li>
	<li><b>Kiindulási állapot (beginning) és a cél (destination):</b> Honnan indultunk és hová akarunk eljutni.</li>
	<li><b>Kezdődátum (start) és végdátum (deadline):</b> Mikor indult a projekt, meddig szeretnénk befejezni.</li>
	</ul>	
	
	
	<h5>Projekt-terv</h5>
	A projekthez különböző kategóriák (pl. megbeszélés, design, fejlesztés) és emberek rendelhetőek.<br/>
	Ezt követően a projekt tervezője megtervezi, hogy melyik emberre, melyik kategóriában mennyi időt szán.<br/>
	A munkatársak ezt követően munkájuk során ezt az időt fogják "elhasználni."
	
	<h5>Projekt létrehozása</h5>
	<p>Projektet bárki létrehozhat a +New Project menüpont vagy a projekt duplikálás segítségével. Ekkor a projekt azonban még csak Requested státuszú lesz, és egy adminisztrátor kell, hogy jóváhagyja. Miután a projekt aktív lesz, már csak az adminisztrátor tudja módosítani.</p>
	
	 
